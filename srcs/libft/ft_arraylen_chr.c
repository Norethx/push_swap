#include "libft.h"

size_t	ft_arraylen_chr(char **array)
{
	int	i[2];

	i[0] = 0;
	i[1] = 0;
	while (array[i[0]])
		i[1] += ft_strlen(array[i[0]++]);
	return ((size_t)i[1]);
}
